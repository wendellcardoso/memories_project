import mongoose from 'mongoose';

const postSchma = mongoose.Schema({
    title: String,
    message: String,
    creator: String,
    tags: [String],
    selectedFile: String,
    likeCount: {
        type: Number,
        default: 0
    },
    createdAt: {
        type: Date,
        default: new Date()
    },
});

const Post = new mongoose.model('Post', postSchma);

export default Post;